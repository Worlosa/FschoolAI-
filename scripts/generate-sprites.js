// ── Character generator ───────────────────────────────────────────────────────
// Draws a 16×16 top-down seated student. Two animation frames side-by-side
// in a 32×16 spritesheet. Typing animation: arms move slightly between frames.

function drawCharacter(shirtC, shirtD, hairC, frame) {
  // Returns a function that draws onto a canvas at offset ox,oy
  return (cvs, ox, oy) => {
    const { set, fill } = cvs;

    // ── Head (rows 1-5 relative, centered around ox+7,8) ─────────────────
    const hx = ox + 5, hy = oy + 1; // head top-left corner in sprite space
    fill(hx+1, hy,   hx+4, hy,   ...hairC);           // top hair row
    fill(hx,   hy+1, hx,   hy+3, ...hairC);            // left hair
    fill(hx+5, hy+1, hx+5, hy+3, ...hairC);            // right hair
    fill(hx+1, hy+4, hx+4, hy+4, ...hairC);            // bottom hair
    fill(hx+1, hy+1, hx+4, hy+3, ...C.SKIN);           // face
    // eyes
    set(hx+1, hy+2, ...C.EYE);
    set(hx+3, hy+2, ...C.EYE);
    // subtle mouth
    set(hx+2, hy+3, ...C.MOUTH);

    // ── Body / shirt (rows 6-9) ──────────────────────────────────────────
    fill(ox+5, oy+6, ox+9, oy+6, ...shirtC);
    fill(ox+4, oy+7, ox+9, oy+7, ...shirtC);
    fill(ox+4, oy+8, ox+9, oy+8, ...shirtD);
    fill(ox+4, oy+9, ox+9, oy+9, ...shirtD);

    // ── Arms (rows 10-11) — two frames differ here ───────────────────────
    if (frame === 0) {
      // idle: arms resting, slightly inward
      set(ox+3, oy+10, ...shirtC); set(ox+10, oy+10, ...shirtC);
      set(ox+2, oy+11, ...C.SKIN); set(ox+3, oy+11, ...shirtC);
      set(ox+10, oy+11, ...shirtC); set(ox+11, oy+11, ...C.SKIN);
    } else {
      // typing: arms forward (1px closer to desk = up)
      set(ox+3, oy+9,  ...shirtC); set(ox+10, oy+9,  ...shirtC);
      set(ox+2, oy+10, ...C.SKIN); set(ox+3, oy+10, ...shirtC);
      set(ox+10, oy+10, ...shirtC); set(ox+11, oy+10, ...C.SKIN);
    }

    // ── Chair back (visible above seated person, rows 0-1) ──────────────
    fill(ox+5, oy+0, ox+9, oy+0, ...C.CHAIR);
    set(ox+4, oy+0, ...C.CHAIR_D); set(ox+10, oy+0, ...C.CHAIR_D);

    // ── Chair seat (rows 12-14) ──────────────────────────────────────────
    fill(ox+4, oy+12, ox+9, oy+12, ...C.CHAIR_D);
    fill(ox+3, oy+13, ox+10, oy+13, ...C.CHAIR);
    fill(ox+4, oy+14, ox+9, oy+14, ...C.CHAIR_D);
  };
}

// Writes a 32×16 sprite sheet: frame0 at x=0, frame1 at x=16
function makeCharSheet(name, shirtC, shirtD, hairC) {
  const cvs = canvas(32, 16);
  drawCharacter(shirtC, shirtD, hairC, 0)(cvs, 0, 0);  // frame 0: idle
  drawCharacter(shirtC, shirtD, hairC, 1)(cvs, 16, 0); // frame 1: typing
  cvs.save(path.join(OUT_CHARS, `${name}.png`));
  console.log(`  ✓ characters/${name}.png`);
}

